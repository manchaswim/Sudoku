    /**
     * Metodo: Agrega Digito
     * @param numero
     * @param ren
     * @param col
     * @return boolean si se pudo o no agregar el digito
     */
    public boolean agregaDigito(int numero, int ren, int col){
        boolean resp=false;
        if(numeroValido(numero,ren,col)){
            mat[ren][col]=numero;
            resp=true;
        }
        return resp;
    }
    /**
     * Metodo:Numero Valido, llama a verifica area para saber si es valido
     * @param numero
     * @param ren
     * @param col
     * @return boolean del metodo verifica area
     */
    private boolean numeroValido(int numero, int ren, int col){
        return verificaArea(numero,ren,col)&&verificaRenglon(numero,ren) && verificaColumna(numero,col);
    }
    /**
     * Metodo: Verifica areas
     * @param numero
     * @param ren
     * @param col
     * @return boolean asegurandose que el digito ingresado cumpla la regla del sudoku
     */
    private boolean verificaArea(int numero,int ren, int col){
        boolean resp=true;
        if(numero<=9 && numero>0){
            int i,j,fR,fC,cuadrante;
           
            if(ren<=2){
                fR=2;
            }
            else
                if(ren<=5){
                    fR=5;
                }
                else
                    fR=8;//Debe estar validado
            
            if(col<=2){
                fC=2;
            }
            else
                if(col<=5){
                    fC=5;
                }
                else
                    fC=8;//Debe estar validado
        
            i=fR-2;
            while(i<=fR &&resp){
                j=fC-2;
                while(j<=fC &&resp){
                    if(mat[i][j]==numero)
                        resp=false;
                    j++;
                }
                i++;
            }
        
        }
        else
            resp=false;
        
        return resp;
    }
    /**
     * Metodo: Verifica Columna
     * @param numero
     * @param c
     * @return boolean si se puede agregar el digito en esa columna
     */
    private boolean verificaColumna(int numero,int c){
        boolean resp=true;
        int i=0;
        while(i<9 && resp){
            if(mat[i][c]==numero)
                resp=false;
            i++;
        }
        return resp;
    }
    /**
     * Metodo: Verifica Renglon
     * @param numero
     * @param r
     * @return boolean si se puede o no agregar el digito en ese renglon
     */
    private boolean verificaRenglon(int numero,int r){
        boolean resp=true;
        int i=0;
        while(i<9 && resp){
            if(mat[r][i]==numero)
                resp=false;
            i++;
        }
        return resp;
    }
    
